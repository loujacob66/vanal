async function onRenderMontage() {
    const ids = [...queue];
    if (!ids.length) return;

    const btn      = document.getElementById("btn-montage-render");
    const status   = document.getElementById("montage-status");
    const closeBtn = document.getElementById("montage-close");
    const filename = document.getElementById("montage-name").value.trim();
    const canvas   = document.getElementById("montage-canvas").value;
    const fit      = document.getElementById("montage-fit").value;
    const reencode = canvas ? true : document.getElementById("montage-reencode").checked;

    btn.disabled = closeBtn.disabled = true;
    closeBtn.style.opacity = "0.3";

    const isEncoding = reencode || canvas;
    let dots = 0;
    const ticker = setInterval(() => {
        dots = (dots + 1) % 4;
        btn.textContent = (isEncoding ? "Encoding" : "Rendering") + ".".repeat(dots);
    }, 600);
    status.textContent = canvas
        ? `Re-encoding to ${canvas} (${fit}) — this may take a while...`
        : reencode ? "Re-encoding — this may take a while..." : "Concatenating clips...";

    try {
        const resp = await fetch("/api/export/render", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ clip_ids: ids, filename, reencode, canvas, fit }),
        });
        const data = await resp.json();
        clearInterval(ticker);

        if (data.error) {
            btn.textContent = "Render";
            btn.disabled = false;
            status.textContent = "";
            alert("Render failed: " + data.error + (data.details ? "\n\n" + data.details : ""));
        } else {
            btn.textContent = "Done ✓";
            status.innerHTML = `Saved <strong>${esc(data.filename)}</strong> (${data.size_mb} MB, ${data.clip_count} clips)` +
                (data.skipped?.length ? ` — ${data.skipped.length} skipped` : "");
        }
    } catch (err) {
        clearInterval(ticker);
        btn.textContent = "Render";
        btn.disabled = false;
        status.textContent = "";
        alert("Render failed: " + err.message);
    } finally {
        closeBtn.disabled = false;
        closeBtn.style.opacity = "1";
    }
}


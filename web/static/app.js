let _lastNotifCount = 0;

function initNotifications() {
    const btn = document.getElementById("btn-notifications");
    const panel = document.getElementById("notification-panel");
    if (!btn || !panel) return;

    btn.addEventListener("click", (e) => {
        e.stopPropagation();
        const visible = panel.style.display !== "none";
        panel.style.display = visible ? "none" : "flex";
        if (!visible) loadNotifications();
    });

    // Close on outside click
    document.addEventListener("click", (e) => {
        if (!panel.contains(e.target) && e.target !== btn && !btn.contains(e.target)) {
            panel.style.display = "none";
        }
    });

    document.getElementById("btn-notif-dismiss-all").addEventListener("click", async () => {
        await fetch("/api/notifications/read", { method: "POST" });
        loadNotifications();
        updateNotifBadge(0);
    });

    // Poll for new notifications every 30s
    pollNotifications();
    setInterval(pollNotifications, 30000);
}

async function pollNotifications() {
    try {
        const resp = await fetch("/api/notifications");
        if (!resp.ok) return;
        const data = await resp.json();
        const count = data.unread_count;
        updateNotifBadge(count);
        // Show toast for new notifications
        if (count > _lastNotifCount && _lastNotifCount >= 0) {
            const newOnes = data.notifications.filter(n => !n.is_read).slice(0, count - _lastNotifCount);
            newOnes.forEach(n => showToast(n.message, "info"));
        }
        _lastNotifCount = count;
    } catch (e) { /* ignore */ }
}

function updateNotifBadge(count) {
    const badge = document.getElementById("notification-badge");
    const btn = document.getElementById("btn-notifications");
    if (!badge) return;
    if (count > 0) {
        badge.textContent = count > 99 ? "99+" : count;
        badge.style.display = "";
        btn.classList.add("has-notifs");
    } else {
        badge.style.display = "none";
        btn.classList.remove("has-notifs");
    }
}

async function loadNotifications() {
    const listEl = document.getElementById("notification-list");
    try {
        const resp = await fetch("/api/notifications");
        if (!resp.ok) return;
        const data = await resp.json();
        updateNotifBadge(data.unread_count);

        if (data.notifications.length === 0) {
            listEl.innerHTML = '<div class="notification-empty">No notifications yet</div>';
            return;
        }

        listEl.innerHTML = data.notifications.map(n => {
            const timeAgo = formatTimeAgo(n.created_at);
            return `
                <div class="notification-item ${n.is_read ? 'read' : 'unread'}" data-notif-id="${n.id}" data-clip-id="${n.clip_id || ''}" data-type="${esc(n.type || '')}">
                    <div class="notif-content">
                        <div class="notif-text">${esc(n.message)}</div>
                        <div class="notif-time">${timeAgo}</div>
                    </div>
                </div>`;
        }).join("");

        listEl.querySelectorAll(".notification-item").forEach(item => {
            item.addEventListener("click", async () => {
                const notifId = item.dataset.notifId;
                const clipId = item.dataset.clipId;
                const type = item.dataset.type;
                // Mark as read
                await fetch(`/api/notifications/${notifId}/read`, { method: "POST" });
                item.classList.remove("unread");
                item.classList.add("read");
                document.getElementById("notification-panel").style.display = "none";

                if (type === "montage_share") {
                    // Open outputs panel directly on shared tab
                    activeOutputTab = "shared";
                    document.getElementById("outputs-overlay").style.display = "flex";
                    await refreshOutputsList();
                    showToast("Viewing shared montages");
                } else if (clipId) {
                    // Navigate to shared tab and open clip
                    activeClipTab = "shared";
                    document.querySelectorAll(".clip-tab").forEach(t => t.classList.toggle("active", t.dataset.clipTab === "shared"));
                    await loadSharedClips();
                    openModal(parseInt(clipId), true);
                }
                pollNotifications();
            });
        });
    } catch (e) {
        listEl.innerHTML = '<div class="notification-empty" style="color:var(--red)">Error loading notifications</div>';
    }
}

function formatTimeAgo(dateStr) {
    if (!dateStr) return "";
    const date = new Date(dateStr + "Z"); // UTC
    const now = new Date();
    const diffMs = now - date;
    const diffMin = Math.floor(diffMs / 60000);
    if (diffMin < 1) return "just now";
    if (diffMin < 60) return `${diffMin}m ago`;
    const diffHr = Math.floor(diffMin / 60);
    if (diffHr < 24) return `${diffHr}h ago`;
    const diffDay = Math.floor(diffHr / 24);
    if (diffDay < 7) return `${diffDay}d ago`;
    return date.toLocaleDateString();
}


async function loadThumbPicker(clip) {
    const picker = document.getElementById("thumb-picker");
    if (!picker) return;

    try {
        const resp = await fetch(`/api/clips/${clip.id}/frames`);
        const data = await resp.json();

        if (!data.frames || data.frames.length === 0) {
            picker.innerHTML = `
                <span class="thumb-picker-empty">No frames on disk.&nbsp;</span>
                <button id="btn-extract-frames" class="btn btn-sm btn-primary">Extract Frames</button>`;
            document.getElementById("btn-extract-frames")?.addEventListener("click", () => requireAuth(async () => {
                const btn = document.getElementById("btn-extract-frames");
                btn.disabled = true;
                btn.textContent = "Extracting…";
                try {
                    const r = await fetch(`/api/clips/${clip.id}/extract-frames`, { method: "POST" });
                    const d = await r.json();
                    if (d.error) { alert(d.error); btn.disabled = false; btn.textContent = "Extract Frames"; }
                    else { await loadThumbPicker(clip); }
                } catch (err) {
                    alert("Failed: " + err.message);
                    btn.disabled = false; btn.textContent = "Extract Frames";
                }
            }));
            return;
        }

        const current = data.thumbnail_frame || "frame_0001.jpg";
        picker.innerHTML = data.frames.map(fname => `
            <div class="thumb-picker-item${fname === current ? " selected" : ""}"
                 data-frame="${fname}" title="${fname}">
                <img src="/frames/${clip.file_hash}/${fname}"
                     onerror="this.parentElement.style.display='none'" alt="${fname}">
            </div>`).join("");

        picker.querySelectorAll(".thumb-picker-item").forEach(item => {
            item.addEventListener("click", () => requireAuth(async () => {
                const frame = item.dataset.frame;
                try {
                    await fetch(`/api/clips/${clip.id}/thumbnail`, {
                        method: "POST",
                        headers: { "Content-Type": "application/json" },
                        body: JSON.stringify({ frame }),
                    });
                    // Update selection highlight
                    picker.querySelectorAll(".thumb-picker-item").forEach(i =>
                        i.classList.toggle("selected", i === item)
                    );
                    // Update in-memory clip and refresh card
                    clip.thumbnail_frame = frame;
                    filterAndRender();
                } catch (err) {
                    alert("Failed to set thumbnail: " + err.message);
                }
            }));
        });
    } catch (err) {
        picker.innerHTML = `<span class="thumb-picker-empty">Could not load frames.</span>`;
    }
}

async function initSharePanel(clipId) {
    const listEl = document.getElementById("share-user-list");
    const selectEl = document.getElementById("share-add-select");
    if (!listEl || !selectEl) return;

    async function refreshShares() {
        try {
            const [sharesResp, usersResp] = await Promise.all([
                fetch(`/api/clips/${clipId}/shares`),
                fetch("/api/clips/shareable-users"),
            ]);
            const shares = await sharesResp.json();
            const allUsers = await usersResp.json();
            const sharedIds = new Set(shares.map(s => s.user_id));

            // Render current shares
            if (shares.length === 0) {
                listEl.innerHTML = '<span style="font-size:0.78rem;color:var(--text2)">Not shared with anyone</span>';
            } else {
                listEl.innerHTML = shares.map(s => `
                    <div class="share-user-row">
                        <div class="share-user-info">
                            <span class="share-user-name">${esc(s.name || "Unknown")}</span>
                            <span class="share-user-email">${esc(s.email)}</span>
                        </div>
                        <button class="btn btn-sm" data-unshare-uid="${s.user_id}" style="color:var(--red);font-size:0.72rem">Remove</button>
                    </div>
                `).join("");

                listEl.querySelectorAll("[data-unshare-uid]").forEach(btn => {
                    btn.addEventListener("click", async () => {
                        const uid = btn.dataset.unshareUid;
                        btn.disabled = true;
                        await fetch(`/api/clips/${clipId}/share/${uid}`, { method: "DELETE" });
                        showToast("Share removed", "success");
                        refreshShares();
                    });
                });
            }

            // Populate add dropdown with non-shared users
            const available = allUsers.filter(u => !sharedIds.has(u.id));
            selectEl.innerHTML = '<option value="">Add a user...</option>' +
                available.map(u => `<option value="${u.id}">${esc(u.name || "Unknown")} (${esc(u.email)})</option>`).join("");
        } catch (err) {
            listEl.innerHTML = `<span style="color:var(--red);font-size:0.78rem">Error loading shares</span>`;
        }
    }

    document.getElementById("btn-share-add").addEventListener("click", async () => {
        const uid = parseInt(selectEl.value);
        if (!uid) return;
        const btn = document.getElementById("btn-share-add");
        btn.disabled = true;
        try {
            await fetch(`/api/clips/${clipId}/share`, {
                method: "POST",
                headers: { "Content-Type": "application/json" },
                body: JSON.stringify({ user_ids: [uid] }),
            });
            showToast("Clip shared", "success");
            refreshShares();
        } catch (err) {
            showToast("Failed to share", "error");
        } finally {
            btn.disabled = false;
        }
    });

    refreshShares();
}


async function initViewAs() {
    const section = document.getElementById("viewas-section");
    const select = document.getElementById("viewas-select");
    try {
        const resp = await fetch("/api/auth/users");
        const users = await resp.json();
        if (users.length < 2) return;

        section.style.display = "block";
        const myId = realAdmin ? realAdmin.id : currentUser?.id;
        for (const u of users) {
            if (u.id === myId) continue;  // don't list yourself
            const opt = document.createElement("option");
            opt.value = u.id;
            opt.textContent = u.name || u.email;
            select.appendChild(opt);
        }

        // If currently impersonating, pre-select
        if (isImpersonating && currentUser) {
            select.value = String(currentUser.id);
        }

        select.addEventListener("change", async () => {
            const userId = select.value;
            if (!userId) {
                // "View as..." selected — stop impersonating
                if (isImpersonating) {
                    await fetch("/api/auth/stop-impersonating", { method: "POST" });
                    window.location.reload();
                }
                return;
            }
            await fetch(`/api/auth/impersonate/${userId}`, { method: "POST" });
            window.location.reload();
        });
    } catch { /* ignore */ }
}


function openFTMGuide(id) {
    const guide = FTM_GUIDES.find(g => g.id === id);
    if (!guide) return;

    let stepNum = 0;
    const stepsHtml = guide.steps.map(s => {
        if (s.tip) {
            return `<div class="ftm-tip">💡 ${formatFTMText(s.tip)}</div>`;
        }
        stepNum++;
        return `<div class="ftm-step"><span class="ftm-step-num">${stepNum}</span><span>${formatFTMText(s.text)}</span></div>`;
    }).join("");

    document.getElementById("ftm-content").innerHTML = `
        <button class="ftm-back" onclick="openFTMModal()">← All topics</button>
        <h2 style="margin-bottom:16px">${guide.icon} ${esc(ftmPrefix)} ${esc(guide.suffix)}</h2>
        <div class="ftm-steps">${stepsHtml}</div>
    `;
}

function formatFTMText(text) {
    return text.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
}

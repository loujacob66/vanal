function applyAuthState() {
    if (!isAuthenticated) {
        // Gate entire app: show login overlay, hide everything else
        document.getElementById("login-overlay").style.display = "flex";
        document.getElementById("loading").textContent = "";
        return;
    }

    // User footer in sidebar
    const footer = document.getElementById("sidebar-user-footer");
    if (footer && currentUser) {
        footer.style.display = "flex";
        document.getElementById("user-name").textContent = currentUser.name || currentUser.email;
        const avatar = document.getElementById("user-avatar");
        if (currentUser.picture_url) {
            avatar.src = currentUser.picture_url;
            avatar.style.display = "inline-block";
        }
    }

    // Impersonation banner
    const banner = document.getElementById("impersonate-banner");
    if (banner && isImpersonating) {
        banner.style.display = "flex";
        document.getElementById("impersonate-name").textContent = currentUser.name || currentUser.email;
    }

    // Admin-only UI (real admin, not while impersonating)
    const isRealAdmin = realAdmin || (currentUser?.is_admin && !isImpersonating);
    const ingestBtn = document.getElementById("btn-ingest");
    if (ingestBtn) ingestBtn.style.display = isRealAdmin ? "block" : "none";
}

